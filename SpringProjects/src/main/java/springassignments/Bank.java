package springassignments;

public class Bank {

	int bankId;
	String bankName;
	 public Bank(int bankId, String bankName) {
	        this.bankId = bankId;
	        this.bankName = bankName;
	    }
	public int getBankId() {
		return bankId;
	}
	
	public String getBankName() {
		return bankName;
	}
	
	
}
